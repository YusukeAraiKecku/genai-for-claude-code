import type { Recipe } from '../recipe/types.js';

export function generateSkillReadme(recipe: Recipe): string {
  const tags = (recipe.tags ?? []).join(', ');
  const refs = recipe.local_context?.references ?? [];
  const tmpls = recipe.local_context?.templates ?? [];

  return `# ${recipe.name}

> ${recipe.description}

- **Skill id**: \`${recipe.id}\`
- **Version**: ${recipe.version}
- **Default execution mode**: \`${recipe.execution.default_mode}\`
- **Tags**: ${tags || '(none)'}

## How to use in Claude Code

After installing this Skill (\`genai install ${recipe.id} --scope project\`), invoke from Claude Code:

\`\`\`text
/${recipe.claude?.skill_name ?? recipe.id} <your request here>
\`\`\`

Claude reads \`SKILL.md\` and follows the Process / Output contract sections.

## Inputs

${Object.entries(recipe.inputs)
  .map(([k, f]) => `- \`${k}\` (${f.type}${f.required ? ', required' : ''}) — ${f.title ?? k}`)
  .join('\n')}

## References

${refs.length === 0 ? '(none)' : refs.map((r) => `- \`${r}\``).join('\n')}

## Templates

${tmpls.length === 0 ? '(none)' : tmpls.map((t) => `- \`${t}\``).join('\n')}

## Safety

\`\`\`text
network:        ${recipe.security?.network ?? 'deny-by-default'}
secrets:        ${recipe.security?.secrets ?? 'forbid-hardcoded'}
side_effects:   ${recipe.security?.side_effects ?? 'read-only-by-default'}
allowed_tools:  ${(recipe.claude?.allowed_tools ?? recipe.security?.allowed_tools ?? ['Read', 'Grep', 'Glob']).join(', ')}
\`\`\`

## Generated by

\`genai compile\` from \`genai.recipe.yml\` (Genai for Claude Code).
`;
}
